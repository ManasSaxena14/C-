#include <iostream>
using namespace std;

int main() {

    int a = 10;
    int b = 3;

    cout << "Addition = " << a + b << endl;
    cout << "Subtraction = " << a - b << endl;
    cout << "Multiplication = " << a * b << endl;
    cout << "Division = " << a / b << endl;
    cout << "Remainder = " << a % b << endl;

    return 0;
}

/*
OUTPUT:
Addition = 13
Subtraction = 7
Multiplication = 30
Division = 3
Remainder = 1
*/
