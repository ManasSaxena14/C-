#include <iostream>
using namespace std;

int main()
{
    int rollNumber = 101;      // integer value
    char section = 'B';        // character
    float marks = 89.5f;       // float value (f used)
    bool placementReady = true; // boolean value
    double cgpa = 9.23456;     // double value

    cout << "Roll Number: " << rollNumber << endl;
    cout << "Section: " << section << endl;
    cout << "Marks: " << marks << endl;
    cout << "Placement Ready: " << placementReady << endl;
    cout << "CGPA: " << cgpa << endl;

    return 0;
}

// output
/*
Roll Number: 101
Section: B
Marks: 89.5
Placement Ready: 1
CGPA: 9.23456
*/
